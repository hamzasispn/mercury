// extract jersey main shape
function extractCls2Path(svg: string): string | null {
  const match = svg.match(/<path[^>]*class="cls-2-[^"]+"[^>]*d="([^"]+)"/)
  return match ? match[1] : null
}

// scope svg classes so multiple kits don't clash
function scopeSvgClasses(svg: string, uid: string) {
  return svg
    .replace(/cls-1/g, `cls-1-${uid}`)
    .replace(/cls-2/g, `cls-2-${uid}`)
    .replace(/cls-3/g, `cls-3-${uid}`)
    .replace(/cls-4/g, `cls-4-${uid}`)
    .replace(/cls-5/g, `cls-5-${uid}`)
}

// apply kit colors safely (checkout + SSR safe)
function applyColors(svg: string, kit: KitCustomization, uid: string) {
  return svg
    .replace(
      new RegExp(`\\.cls-1-${uid}\\s*\\{[\\s\\S]*?fill:\\s*[^;]+;`, "g"),
      `.cls-1-${uid} { fill: ${kit.accentColor};`
    )
    .replace(
      new RegExp(`\\.cls-2-${uid}\\s*\\{[\\s\\S]*?fill:\\s*[^;]+;`, "g"),
      `.cls-2-${uid} { fill: ${kit.primaryColor};`
    )
    .replace(
      new RegExp(`\\.cls-3-${uid}\\s*\\{[\\s\\S]*?fill:\\s*[^;]+;`, "g"),
      `.cls-3-${uid} { fill: ${kit.firstColor };`
    )
}

